public class Dish {
    
//!
private String name;
private Number price;



public Dish(String name, Number price) {
    this.name = name;
    this.price = price;
}



public String getName() {
    return name;
}


public void setName(String name) {
    this.name = name;
}




public Number getPrice() {
    return price;
}




public void setPrice(Number price) {
    this.price = price;
}







}
